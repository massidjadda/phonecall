package LoopsAndOther;

public class GetAndSet {
	private String name;
	private int age;
	private double gpa;
	
	public void setname(String name) {
		this.name = name;
	}
		
	public String getname() {
		return name;
		}
		
	public void setage(int age) {
		this.age = age;
	}
		
	public int getage() { 
		return age;
		}
	
	public void setgpa(double gpa) {
		this.gpa= gpa;
	}
		
	public double getgpa() {
		return gpa;
		}

	



	
}